package com.davita.questionnaire.model;

import com.fasterxml.jackson.annotation.JsonProperty;
import io.swagger.annotations.ApiModel;
import io.swagger.annotations.ApiModelProperty;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import javax.persistence.*;
import java.time.LocalDateTime;

@Entity
@ApiModel( value = "Questionnaire", description = "Questionnaire description")
public class Questionnaire {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    @ApiModelProperty(notes = "Autogenerated Id", dataType = "Integer")
    private Integer id;
    @ApiModelProperty(notes = "Content of the questionnaire", dataType = "String")
    private String content;
    @ApiModelProperty(notes = "Submit datetime of the questionnaire", dataType = "DateTime")
    private LocalDateTime submitDate;

    @OneToOne(fetch = FetchType.EAGER, cascade = CascadeType.ALL)
    @ApiModelProperty(notes = "The person who is submitting the questionnaire", dataType = "Person")
    private Person person;

    public Questionnaire() {
    }

    public Questionnaire(String content, LocalDateTime submitDate, Person person) {
        this.content = content;
        this.submitDate = submitDate;
        this.person = person;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public LocalDateTime getSubmitDate() {
        return submitDate;
    }

    public void setSubmitDate(LocalDateTime submitDate) {
        this.submitDate = submitDate;
    }

    public Person getPerson() {
        return person;
    }

    public void setPerson(Person person) {
        this.person = person;
    }
}
